//---------------------------------------------------------------------------
unsigned int Disasm(const void *code)
{
 const bool bHDEx64	= (THde::DELTA_PREFIXES == SHDE64::DELTA_PREFIXES);  // NOTE: The compiler must optimize out this along with every check of this flag because it is static!

    UINT8 x, c, *p = (UINT8 *)code, cflags, opcode, pref = 0;
    UINT8 *ht  = THde::Table, m_mod, m_reg, m_rm, disp_size = 0;
    UINT8 op64 = 0;

    memset(this,0,sizeof(CHDE));  // NOTE: Replace with 'for' if it causes some include requirements    // Skip this to gain more speed?

    for (x = 16; x; x--)
        switch (c = *p++) {
            case 0xf3:
                this->p_rep = c;
                pref |= PRE_F3;
                break;
            case 0xf2:
                this->p_rep = c;
                pref |= PRE_F2;
                break;
            case 0xf0:
                this->p_lock = c;
                pref |= PRE_LOCK;
                break;
            case 0x26: case 0x2e: case 0x36:
            case 0x3e: case 0x64: case 0x65:
                this->p_seg = c;
                pref |= PRE_SEG;
                break;
            case 0x66:
                this->p_66 = c;
                pref |= PRE_66;
                break;
            case 0x67:
                this->p_67 = c;
                pref |= PRE_67;
                break;
            default:
                goto pref_done;
        }
  pref_done:

    this->flags = (UINT32)pref << 23;

    if (!pref)
        pref |= PRE_NONE;

    if (bHDEx64 && ((c & 0xf0) == 0x40)) {
        this->flags |= F_PREFIX_REX;
        if ((this->rex_w = (c & 0xf) >> 3) && (*p & 0xf8) == 0xb8)
            op64++;
        this->rex_r = (c & 7) >> 2;
        this->rex_x = (c & 3) >> 1;
        this->rex_b = c & 1;
        if (((c = *p++) & 0xf0) == 0x40) {
            opcode = c;
            goto error_opcode;
        }
    }

    if ((this->opcode = c) == 0x0f) {
        this->opcode2 = c = *p++;
        ht += THde::DELTA_OPCODES;
    } else if (c >= 0xa0 && c <= 0xa3) {
        op64++;
        if (pref & PRE_67)
            pref |= PRE_66;
        else
            pref &= ~PRE_66;
    }

    opcode = c;
    cflags = ht[ht[opcode / 4] + (opcode % 4)];

    if (cflags == C_ERROR) {
      error_opcode:
        this->flags |= F_ERROR | F_ERROR_OPCODE;
        cflags = 0;
        if ((opcode & -3) == 0x24)
            cflags++;
    }

    x = 0;
    if (cflags & C_GROUP) {
        UINT16 t;
        t = *(UINT16 *)(ht + (cflags & 0x7f));
        cflags = (UINT8)t;
        x = (UINT8)(t >> 8);
    }

    if (this->opcode2) {
        ht = THde::Table + THde::DELTA_PREFIXES;
        if (ht[ht[opcode / 4] + (opcode % 4)] & pref)
            this->flags |= F_ERROR | F_ERROR_OPCODE;
    }

    if (cflags & C_MODRM) {
        this->flags |= F_MODRM;
        this->modrm = c = *p++;
        this->modrm_mod = m_mod = c >> 6;
        this->modrm_rm = m_rm = c & 7;
        this->modrm_reg = m_reg = (c & 0x3f) >> 3;

        if (x && ((x << m_reg) & 0x80))
            this->flags |= F_ERROR | F_ERROR_OPCODE;

        if (!this->opcode2 && opcode >= 0xd9 && opcode <= 0xdf) {
            UINT8 t = opcode - 0xd9;
            if (m_mod == 3) {
                ht = THde::Table + THde::DELTA_FPU_MODRM + t*8;
                t = ht[m_reg] << m_rm;
            } else {
                ht = THde::Table + THde::DELTA_FPU_REG;
                t = ht[t] << m_reg;
            }
            if (t & 0x80)
                this->flags |= F_ERROR | F_ERROR_OPCODE;
        }

        if (pref & PRE_LOCK) {
            if (m_mod == 3) {
                this->flags |= F_ERROR | F_ERROR_LOCK;
            } else {
                UINT8 *table_end, op = opcode;
                if (this->opcode2) {
                    ht = THde::Table + THde::DELTA_OP2_LOCK_OK;
                    table_end = ht + THde::DELTA_OP_ONLY_MEM - THde::DELTA_OP2_LOCK_OK;
                } else {
                    ht = THde::Table + THde::DELTA_OP_LOCK_OK;
                    table_end = ht + THde::DELTA_OP2_LOCK_OK - THde::DELTA_OP_LOCK_OK;
                    op &= -2;
                }
                for (; ht != table_end; ht++)
                    if (*ht++ == op) {
                        if (!((*ht << m_reg) & 0x80))
                            goto no_lock_error;
                        else
                            break;
                    }
                this->flags |= F_ERROR | F_ERROR_LOCK;
              no_lock_error:
                ;
            }
        }

        if (this->opcode2) {
            switch (opcode) {
                case 0x20: case 0x22:
                    m_mod = 3;
                    if (m_reg > 4 || m_reg == 1)
                        goto error_operand;
                    else
                        goto no_error_operand;
                case 0x21: case 0x23:
                    m_mod = 3;
                    if (m_reg == 4 || m_reg == 5)
                        goto error_operand;
                    else
                        goto no_error_operand;
            }
        } else {
            switch (opcode) {
                case 0x8c:
                    if (m_reg > 5)
                        goto error_operand;
                    else
                        goto no_error_operand;
                case 0x8e:
                    if (m_reg == 1 || m_reg > 5)
                        goto error_operand;
                    else
                        goto no_error_operand;
            }
        }

        if (m_mod == 3) {
            UINT8 *table_end;
            if (this->opcode2) {
                ht = THde::Table + THde::DELTA_OP2_ONLY_MEM;
                table_end = ht + sizeof(THde::Table) - THde::DELTA_OP2_ONLY_MEM;    
            } else {
                ht = THde::Table + THde::DELTA_OP_ONLY_MEM;
                table_end = ht + THde::DELTA_OP2_ONLY_MEM - THde::DELTA_OP_ONLY_MEM;
            }
            for (; ht < table_end; ht += 2)     // Fix: 'ht != table_end' crash on '41 0F B6 C6  movzx eax, r14b'
                if (*ht++ == opcode) {
                    if (*ht++ & pref && !((*ht << m_reg) & 0x80))
                        goto error_operand;
                    else
                        break;
                }
            goto no_error_operand;
        } else if (this->opcode2) {
            switch (opcode) {
                case 0x50: case 0xd7: case 0xf7:
                    if (pref & (PRE_NONE | PRE_66))
                        goto error_operand;
                    break;
                case 0xd6:
                    if (pref & (PRE_F2 | PRE_F3))
                        goto error_operand;
                    break;
                case 0xc5:
                    goto error_operand;
            }
            goto no_error_operand;
        } else
            goto no_error_operand;

      error_operand:
        this->flags |= F_ERROR | F_ERROR_OPERAND;
      no_error_operand:

        c = *p++;
        if (m_reg <= 1) {
            if (opcode == 0xf6)
                cflags |= C_IMM8;
            else if (opcode == 0xf7)
                cflags |= C_IMM_P66;
        }

        switch (m_mod) {
            case 0:
                if (pref & PRE_67) {
                    if (m_rm == 6)
                        disp_size = 2;
                } else
                    if (m_rm == 5)
                        disp_size = 4;
                break;
            case 1:
                disp_size = 1;
                break;
            case 2:
                disp_size = 2;
                if (!(pref & PRE_67))
                    disp_size <<= 1;
        }

        if ((m_mod != 3 && m_rm == 4) && (bHDEx64 || !(pref & PRE_67))) {	 // 'if(m_mod != 3 && m_rm == 4 && !(pref & PRE_67))' for x32
            this->flags |= F_SIB;
            p++;
            this->sib = c;
            this->sib_scale = c >> 6;
            this->sib_index = (c & 0x3f) >> 3;
            if ((this->sib_base = c & 7) == 5 && !(m_mod & 1))
                disp_size = 4;
        }

        p--;
        switch (disp_size) {
            case 1:
                this->flags |= F_DISP8;
                this->disp.disp8 = *p;
                break;
            case 2:
                this->flags |= F_DISP16;
                this->disp.disp16 = *(UINT16 *)p;
                break;
            case 4:
                this->flags |= F_DISP32;
                this->disp.disp32 = *(UINT32 *)p;
        }
        p += disp_size;
    } else if (pref & PRE_LOCK)
        this->flags |= F_ERROR | F_ERROR_LOCK;

    if (cflags & C_IMM_P66) {
        if (cflags & C_REL32) {
            if (pref & PRE_66) {
                this->flags |= F_IMM16 | F_RELATIVE;
                this->imm.imm16 = *(UINT16 *)p;
                p += 2;
                goto disasm_done;
            }
            goto rel32_ok;
        }

	 if(bHDEx64)
	  {
        if (op64) {
            this->flags |= F_IMM64;
            this->imm.imm64 = *(UINT64 *)p;
            p += 8;
        } else if (!(pref & PRE_66)) {
            this->flags |= F_IMM32;
            this->imm.imm32 = *(UINT32 *)p;
            p += 4;
        } else goto imm16_ok;
	  }
	   else
	    {
          if (pref & PRE_66) {
            this->flags |= F_IMM16;
            this->imm.imm16 = *(UINT16 *)p;
            p += 2;
          } else {
            this->flags |= F_IMM32;
            this->imm.imm32 = *(UINT32 *)p;
            p += 4;
          }	
		}
    }


    if (cflags & C_IMM16) {
	  if(bHDEx64)
	   {
      imm16_ok:
        this->flags |= F_IMM16;
        this->imm.imm16 = *(UINT16 *)p;
	   }
	    else
		 {
          if (this->flags & F_IMM32) {
            this->flags |= F_IMM16;
            this->disp.disp16 = *(UINT16 *)p;
          } else if (this->flags & F_IMM16) {
            this->flags |= F_2IMM16;
            this->disp.disp16 = *(UINT16 *)p;
          } else {
            this->flags |= F_IMM16;
            this->imm.imm16 = *(UINT16 *)p;
          }		 
		 }
        p += 2;	   
    }
    if (cflags & C_IMM8) {
        this->flags |= F_IMM8;
        this->imm.imm8 = *p++;
    }

    if (cflags & C_REL32) {
      rel32_ok:
        this->flags |= F_IMM32 | F_RELATIVE;
        this->imm.imm32 = *(UINT32 *)p;
        p += 4;
    } else if (cflags & C_REL8) {
        this->flags |= F_IMM8 | F_RELATIVE;
        this->imm.imm8 = *p++;
    }

  disasm_done:

    if ((this->len = (UINT8)(p-(UINT8 *)code)) > 15) {
        this->flags |= F_ERROR | F_ERROR_LENGTH;
        this->len = 15;
    }

    return (unsigned int)this->len;
}
